    public void limpaCampos() {
        jTNome.setText("");
        jTLogin.setText("");
        jTSenha.setText("");

        jTNome.requestFocus();
    }

    private void jTUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTUsuariosMouseClicked
        Usuarios us = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
        jTNome.setText(us.getNome());
        jTLogin.setText(us.getLogin());
        jTSenha.setText(us.getSenha());
        jCTipo.setSelectedItem(us.getTipo());
    }//GEN-LAST:event_jTUsuariosMouseClicked

    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        Usuarios us = new Usuarios();
        UsuariosDAO dao = new UsuariosDAO();

        try {
            us.setNome(jTNome.getText());
            us.setLogin(jTLogin.getText());
            us.setSenha(jTSenha.getText());
            us.setTipo(jCTipo.getSelectedItem().toString());
            dao.create(us);
            modelo.recarregaTabela();
            limpaCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar" + e);
        }
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jBAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAlterarActionPerformed
        if (jTUsuarios.getSelectedRow() != -1) {
            modelo.setValueAt(jTNome.getText(), jTUsuarios.getSelectedRow(), 0);
            modelo.setValueAt(jTLogin.getText(), jTUsuarios.getSelectedRow(), 1);
            modelo.setValueAt(jTSenha.getText(), jTUsuarios.getSelectedRow(), 2);
            modelo.setValueAt(jCTipo.getSelectedItem(), jTUsuarios.getSelectedRow(), 3);

            Usuarios us = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
            UsuariosDAO dao = new UsuariosDAO();
            dao.update(us);
            limpaCampos();
            modelo.recarregaTabela();
        }
    }//GEN-LAST:event_jBAlterarActionPerformed

    private void jBRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBRemoverActionPerformed
        if (jTUsuarios.getSelectedRow() != -1) {
            Usuarios us = modelo.pegaDadosLinha(jTUsuarios.getSelectedRow());
            UsuariosDAO dao = new UsuariosDAO();
            dao.delete(us);
            modelo.recarregaTabela();
        }
    }//GEN-LAST:event_jBRemoverActionPerformed

